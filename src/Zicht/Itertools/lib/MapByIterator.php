/**
 * Class MapByIterator
 * @package iter
 */
class MapByIterator extends \IteratorIterator implements \ArrayAccess, \Countable
{
    use ArrayAccessTrait;
    use CountableTrait;
    use DebugInfoTrait;
    use GetterTrait;

    // Fluent interface traits
    use AllTrait;
    use AnyTrait;
    use ChainTrait;
    use CycleTrait;
    use FilterTrait;
    use FirstTrait;
    use GroupByTrait;
    use ItemsTrait;
    use KeysTrait;
    use LastTrait;
    use MapByTrait;
    use MapTrait;
    use ReduceTrait;
    use ReversedTrait;
    use SliceTrait;
    use SortedTrait;
    use ToArrayTrait;
    use UniqueTrait;
    use ValuesTrait;
    use ZipTrait;

    /**
     * @var callable
     */
    private $func;

    /**
     * @param callable $func
     * @param \Iterator $iterable
     */
    public function __construct(\Closure $func, \Iterator $iterable)
    {
        parent::__construct($iterable);
        $this->func = $func;
    }

    /**
     * @return mixed
     */
    public function key()
    {
        return call_user_func($this->func, $this->current());
    }
}
